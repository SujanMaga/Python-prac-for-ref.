a = [3, 34,32,12,33,42,53,21,11,42,100,18,89,100]
print(a)
# sorts list
a.sort()
print(a)

# reverse list
a.reverse()
print(a)
# adds at the end of list
a.append(1)
print(a)
# inserts value in index mentioned in first parameter (inserts 523 in 0 index)
a.insert(0,523)
print(a)
# removes value in index 2
a.pop(2)
print(a)
# removes 100 value
a.remove(100)
print(a)